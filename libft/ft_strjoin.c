#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)

{
	size_t	len1;
	size_t	len2;
	char	*join;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	join = malloc(sizeof(char) * (len1 + len2 + 1));
	if (!join)
		return (NULL);
	ft_strlcpy(join, s1, len1 + len2 + 1);
	ft_strlcat(join, s2, len1 + len2 + 1);
	return (join);
}
